def add_numbers(a, b):
    """Suma dos números."""
    return a + b

def multiply_numbers(a, b):
    """Multiplica dos números."""
    return a * b

if __name__ == "__main__":
    result_add = add_numbers(5, 3)
    print(f"Suma: {result_add}")

    result_multiply = multiply_numbers(5, 3)
    print(f"Producto: {result_multiply}")